// External library for greeting based on the first letter of name
(function(window) {
    var greeting = {};

    greeting.sayHello = function(name) {
        console.log("Hello " + name);
    };

    greeting.sayGoodbye = function(name) {
        console.log("Goodbye " + name);
    };

    window.greeting = greeting;
})(window);

// Fix the error with the names array and loop through it correctly
(function() {
    var names = ["John", "Jane", "James", "Mark", "Jenny", "Martha", "Lucas"];

    for (var i = 0; i < names.length; i++) {
        var firstLetter = names[i].charAt(0).toLowerCase();
        
        if (firstLetter === 'j') {
            greeting.sayGoodbye(names[i]);
        } else {
            greeting.sayHello(names[i]);
        }
    }
})();
